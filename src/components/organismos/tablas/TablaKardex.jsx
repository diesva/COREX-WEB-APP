export function TablaKardex({
  data,
  SetopenRegistro,
  setdataSelect,
  setAccion,
}) {
  if (data?.length == 0) return null;

  const [pagina, setPagina] = useState(1);
  const [datas, setData] = useState(data);
  const [columnFilters, setColumnFilters] = useState([]);

  const { eliminarCategoria } = useCategoriasStore();

  function eliminar(p) {
    Swal.fire({
      title: "쮼st치s seguro?",
      text: "Una vez eliminado, 춰no podr치 recuperar este registro!",
      icon: "warning",
      showCancelButton: true,
      confirmButtonColor: "#3085d6",
      cancelButtonColor: "#d33",
      confirmButtonText: "Si, eliminar",
    }).then(async (result) => {
      if (result.isConfirmed) {
        console.log(p);
        await eliminarCategoria({ id: p });
      }
    });
  }

  function editar(data) {
    SetopenRegistro(true);
    setdataSelect(data);
    setAccion("Editar");
  }

  // Funci칩n para imprimir la tabla
  function imprimirTabla() {
    const printWindow = window.open("", "_blank");
    printWindow.document.write(`
      <html>
        <head>
          <title>COREX - Impresi칩n Kardex</title>
          <style>
            body { font-family: Arial, sans-serif; margin: 20px; }
            table { width: 100%; border-collapse: collapse; }
            th, td { border: 1px solid #ddd; padding: 8px; text-align: center; }
            th { background-color: #f2f2f2; }
            h1 { text-align: center; }
            .salida { color: #ed4d4d; font-weight: bold; }
            .entrada { color: #30c85b; font-weight: bold; }
          </style>
        </head>
        <body>
          <h1>Kardex</h1>
          <table>
            <thead>
              <tr>
                <th>Producto</th>
                <th>Fecha</th>
                <th>Tipo</th>
                <th>Motivo</th>
                <th>Usuario</th>
                <th>Cantidad</th>
                <th>Stock</th>
              </tr>
            </thead>
            <tbody>
              ${table
                .getRowModel()
                .rows.map(
                  (row) => `
                <tr>
                  <td>${row.original.descripcion}</td>
                  <td>${row.original.fecha}</td>
                  <td class="${row.original.tipo === "salida" ? "salida" : "entrada"}">${
                    row.original.tipo
                  }</td>
                  <td>${row.original.detalle}</td>
                  <td>${row.original.nombres}</td>
                  <td>${row.original.cantidad}</td>
                  <td>${row.original.stock}</td>
                </tr>
              `
                )
                .join("")}
            </tbody>
          </table>
        </body>
      </html>
    `);
    printWindow.document.close();
    printWindow.print();
  }

  const columns = [
    {
      accessorKey: "descripcion",
      header: "Producto",
      cell: (info) => <span>{info.getValue()}</span>,
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "fecha",
      header: "Fecha",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Fecha" className="ContentCell">
          <span>{info.getValue()}</span>
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "tipo",
      header: "Tipo",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Tipo" className="ContentCell">
          {info.getValue() === "salida" ? (
            <Colorcontent color="#ed4d4d" className="contentCategoria">
              {info.getValue()}
            </Colorcontent>
          ) : (
            <Colorcontent color="#30c85b" className="contentCategoria">
              {info.getValue()}
            </Colorcontent>
          )}
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "detalle",
      header: "Motivo",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Motivo" className="ContentCell">
          <span>{info.getValue()}</span>
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "nombres",
      header: "Usuario",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Usuario" className="ContentCell">
          <span>{info.getValue()}</span>
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "cantidad",
      header: "Cantidad",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Cantidad" className="ContentCell">
          <span>{info.getValue()}</span>
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
    {
      accessorKey: "stock",
      header: "Stock",
      enableSorting: false,
      cell: (info) => (
        <td data-title="Stock" className="ContentCell">
          <span>{info.getValue()}</span>
        </td>
      ),
      enableColumnFilter: true,
      filterFn: (row, columnId, filterStatuses) => {
        if (filterStatuses.length === 0) return true;
        const status = row.getValue(columnId);
        return filterStatuses.includes(status?.id);
      },
    },
  ];

  const table = useReactTable({
    data,
    columns,
    initialState: {
      pageSize: 20, // Cambiado de 10 a 20 칤tems por p치gina
    },
    state: {
      columnFilters,
    },
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    columnResizeMode: "onChange",
    meta: {
      updateData: (rowIndex, columnId, value) =>
        setData((prev) =>
          prev.map((row, index) =>
            index === rowIndex
              ? {
                  ...prev[rowIndex],
                  [columnId]: value,
                }
              : row
          )
        ),
    },
  });

  return (
    <Container>
      {/* Bot칩n de imprimir (encima de la tabla) */}
      <ButtonImprimir onClick={imprimirTabla}>Imprimir</ButtonImprimir>
      <table className="responsive-table">
        <thead>
          {table.getHeaderGroups().map((headerGroup) => (
            <tr key={headerGroup.id}>
              {headerGroup.headers.map((header) => (
                <th key={header.id}>
                  {header.column.columnDef.header}
                  {header.column.getCanSort() && (
                    <span
                      style={{ cursor: "pointer" }}
                      onClick={header.column.getToggleSortingHandler()}
                    >
                      <FaArrowsAltV />
                    </span>
                  )}
                  {
                    {
                      asc: " 游댶",
                      desc: " 游댷",
                    }[header.column.getIsSorted()]
                  }
                  <div
                    onMouseDown={header.getResizeHandler()}
                    onTouchStart={header.getResizeHandler()}
                    className={`resizer ${
                      header.column.getIsResizing() ? "isResizing" : ""
                    }`}
                  />
                </th>
              ))}
            </tr>
          ))}
        </thead>
        <tbody>
          {table.getRowModel().rows.map((item) => (
            <tr key={item.id}>
              {item.getVisibleCells().map((cell) => (
                <td key={cell.id}>
                  {flexRender(cell.column.columnDef.cell, cell.getContext())}
                </td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
      <Paginacion
        table={table}
        irinicio={() => table.setPageIndex(0)}
        pagina={table.getState().pagination.pageIndex + 1}
        setPagina={setPagina}
        maximo={table.getPageCount()}
      />
    </Container>
  );
}

